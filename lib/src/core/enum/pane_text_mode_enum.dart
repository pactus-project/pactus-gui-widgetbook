/// [PaneTextMode] Documentation :
/// Defines different text display modes for a pane,
/// indicating whether it is enabled or disabled.
///
/// **Modes:**
/// - [PaneTextMode.enabled] → Active state (default)
/// - [PaneTextMode.disabled] → Inactive state (disabled mode)
enum PaneTextMode {
  /// Enabled mode, indicating an active state.
  enabled,

  /// Disabled mode, indicating an inactive state.
  disabled;
}
